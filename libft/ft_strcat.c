#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*buff;

	buff = s1;
	while (*buff)
		buff++;
	while (*s2)
		*(buff++) = *(s2++);
	*buff = '\0';
	return (s1);
}
